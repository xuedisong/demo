package com.cloudhua.imageplatform.domain;

/**
 * Created by yangchao on 2017/8/15.
 * 杆塔类型类
 */
public class Type {
    private long typeId;
    private String name;
    private String attr;

    public Type(){}

    public Type(long typeId, String name, String attr) {
        this.typeId = typeId;
        this.name = name;
        this.attr = attr;
    }

    public long getTypeId() {
        return typeId;
    }

    public void setTypeId(long typeId) {
        this.typeId = typeId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAttr() {
        return attr;
    }

    public void setAttr(String attr) {
        this.attr = attr;
    }
}
