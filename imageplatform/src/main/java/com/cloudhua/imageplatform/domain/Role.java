package com.cloudhua.imageplatform.domain;

/**
 * Created by yangchao on 2017/8/17.
 * 角色类，角色对应的每一条权限在RightConfig配置
 */
public class Role {

    private long roleId;

    private String name;

    private String displayName;

    private String attr;

    private long right;

    public Role() {
    }

    public Role(String name, String displayName, String attr, long right) {
        this.name = name;
        this.displayName = displayName;
        this.attr = attr;
        this.right = right;
    }

    public Role(long roleId, String name, String displayName, String attr, long right) {
        this.roleId = roleId;
        this.name = name;
        this.displayName = displayName;
        this.attr = attr;
        this.right = right;
    }

    public long getRight() {
        return right;
    }

    public void setRight(long right) {
        this.right = right;
    }

    public long getRoleId() {
        return roleId;
    }

    public void setRoleId(long roleId) {
        this.roleId = roleId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDisplayName() {
        return displayName;
    }

    public void setDisplayName(String displayName) {
        this.displayName = displayName;
    }

    public String getAttr() {
        return attr;
    }

    public void setAttr(String attr) {
        this.attr = attr;
    }
}
